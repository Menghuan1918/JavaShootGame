package com.example.airplaneshoot;

public class Shoot {
    int ShootX;
    int ShootY;
    int ShootSpeed;
    float ShootDamage;

    public Shoot(int ShootX, int ShootY, int ShootSpeed, float ShootDamage) {
        this.ShootX = ShootX;
        this.ShootY = ShootY;
        this.ShootSpeed = ShootSpeed;
        this.ShootDamage = ShootDamage;
    }
}
